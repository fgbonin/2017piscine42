int	ft_antidote(int i, int j, int k)
{
	int a;

	if ((j <= i && i <= k) || (k <= i && i <= j))
		a = i;
	else if ((j <= k && k <= i) || (i <= k && k <= j))
		a = k;
	else if ((i <= j && j <= k) || (k <= j && j <= i))
		a = j;
	else if (i == j && j == k)
		a = i;
	return (a);
}
